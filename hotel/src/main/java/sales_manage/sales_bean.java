package sales_manage;

public class sales_bean {
	
	  String month;
	  int salesEA;
	  int rsvEA;

	  public String getMonth() {
	    return month;
	  }

	  public void setMonth(String month) {
	    this.month = month;
	  }

	  public int getSalesEA() {
	    return salesEA;
	  }

	  public void setSalesEA(int salesEA) {
	    this.salesEA = salesEA;
	  }

	  public int getRsvEA() {
	    return rsvEA;
	  }

	  public void setRsvEA(int rsvEA) {
	    this.rsvEA = rsvEA;
	  }
}
